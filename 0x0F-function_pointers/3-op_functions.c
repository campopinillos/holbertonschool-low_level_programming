/**
 * op_add - Return the sum of a and b
 * @a: First int input
 * @b: Second int input
 *
 * Description: This function returns the
 * sum of a and b
 * Return: The sum of the input parameters
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns the difference of a and b
 * @a: First int input
 * @b: Second int input
 *
 * Description: This function returns the
 * difference of a and b
 * Return: The difference of the input parameters
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Returns the product of a and b
 * @a: First int input
 * @b: Second int input
 *
 * Description: This function returns the
 * product of a and b
 * Return: The product of the input parameters
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Returns the division of a by b
 * @a: First int input
 * @b: Second int input
 *
 * Description: This function returns the result
 * of the division of a by b
 * Return: The division of the input parameters
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns the remainder of a by b
 * @a: First int input
 * @b: Second int input
 *
 * Description: This function returns the remainder
 * of the division of a by b
 * Return: The remainder of the input parameters
 */
int op_mod(int a, int b)
{
	return (a % b);
}
